let index = 0;

function atualizarSlide() {
  document.getElementById("imagem").src = slides[index].imagem;
  document.getElementById("descricao").textContent = slides[index].descricao;

  document.getElementById("btnAnterior").disabled = index === 0;
  document.getElementById("btnProximo").disabled = index === slides.length - 1;
}

function proximo() {
  if (index < slides.length - 1) {
    index++;
    atualizarSlide();
  }
}

function voltar() {
  if (index > 0) {
    index--;
    atualizarSlide();
  }
}

window.onload = atualizarSlide;
